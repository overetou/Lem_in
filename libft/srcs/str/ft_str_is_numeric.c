#include "libft.h"

int				ft_str_is_numeric(char *str)
{
	int		i;
	char	cur;

	i = 0;
	if (str[0] == '-')
		i++;
	while (str[i])
	{
		cur = str[i];
		if (!(cur >= '0' && cur <= '9'))
			return (0);
		i++;
	}
	return (1);
}
